CREATE TABLE projects (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   number VARCHAR(255),
   name VARCHAR(255),
   CONSTRAINT pk_projects PRIMARY KEY (id)
);

ALTER TABLE projects ADD CONSTRAINT uc_projects_number UNIQUE (number);

CREATE TABLE elements (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   location VARCHAR(255),
   label VARCHAR(255),
   side VARCHAR(255),
   material VARCHAR(255),
   grade VARCHAR(255),
   width INTEGER NOT NULL,
   height INTEGER NOT NULL,
   length INTEGER NOT NULL,
   quantity INTEGER NOT NULL,
   level VARCHAR(255),
   project_id BIGINT NOT NULL,
   CONSTRAINT pk_elements PRIMARY KEY (id)
);

ALTER TABLE elements ADD CONSTRAINT FK_ELEMENTS_ON_PROJECT FOREIGN KEY (project_id) REFERENCES projects (id) ON DELETE CASCADE;